#include "io/io.h"

void pic_remap() {
    // Инициализация контроллеров PIC
    outb(0x20, 0x11);
    outb(0xA0, 0x11);

    // Маппим прерывания: IRQ 0-7 пойдут на векторы 32-39
    outb(0x21, 0x20);
    outb(0xA1, 0x28);

    // Доп. настройки
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);

    outb(0x21, 0x0);
    outb(0xA1, 0x0);
}